public class JokeTextInfo {
  public String ct;
  public String id;
  public String text;
  public String title;
  public String type;

  public String getCt() {
    return ct;
  }

  public void setCt(String ct) {
    this.ct = ct;
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getText() {
    return text;
  }

  public void setText(String text) {
    this.text = text;
  }

  public String getTitle() {
    return title;
  }

  public void setTitle(String title) {
    this.title = title;
  }

  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }
}
